// Arithmetic operatos
#include <iostream>
using namespace std;
  int num1 = 20;
  int num2 = 35;
int main()
{
    cout << "num1++ is " << num1++ << endl;
    cout << "++num1 is " << ++num1 << endl;

    cout << "num2-- is " << num2-- << endl;
    cout << "--num2 is " << --num2 << endl;

    return 0;
}
